const ORDERED_REPLACEMENTS = [...REPLACEMENTS].sort((a, b) => b[0].length - a[0].length);

export function koText(value: string | number | null | undefined): string {
  if (value === null || value === undefined) return '';
  const raw = String(value);
  if (EXACT_LABELS[raw]) return EXACT_LABELS[raw];
  let out = raw;
  for (const [from, to] of ORDERED_REPLACEMENTS) {
    out = out.split(from).join(to);
  }
  return out;
}

export function koDept(value: string | null | undefined): string {
  if (!value) return '';
  return DEPT_LABELS[value] || koText(value);
}

export function koRole(value: string | null | undefined): string {
  if (!value) return '';
  return ROLE_LABELS[value] || koText(value);
}

export function koAgent(value: string | null | undefined): string {
  if (!value) return '';
  return DEPT_LABELS[value] || ROLE_LABELS[value] || koText(value);
}
